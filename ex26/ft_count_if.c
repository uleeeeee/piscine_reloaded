int	ft_count_if(char **tab, int(*f)(char*))
{
	int	ones;
	int	i;

	i = 0;
	ones = 0;
	while (tab[i] != '\0')
	{	
		if (f(tab[i]) == 1)
		{
			ones++;
		}
		i++;
	}
	return (ones);
}
